package Calculator;

public class Casio {
    public static double Multiply(double x, double y) {
        return x * y;
    }

    public static double Divide(double x, double y) {
        return x / y;
    }

    public double Sum(double x, double y) {
        return x + y;
    }

    public double Extract(double x, double y) {
        return x - y;
    }


}
